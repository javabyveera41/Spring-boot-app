package com.fdgfgjfdg;

public class SwitchCase {

	public static void main(String[] args) 
	{
		int marks=100;
		switch(marks)
		{
		case 1:
			System.out.println("i am ashok");
			break;
		case 100:
			System.out.println("i am anil");
			default:
			System.out.println("i am adhikari");
		}
	

	}

}
